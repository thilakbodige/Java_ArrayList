package List;

import java.util.Stack;

public class Stack_Peak
{
	public static void main(String[] args)
	{
		Stack<Integer> s=new Stack<>();
	    s.add(10);
	    s.add(20);
	    s.add(30);
	    s.add(40);
	    s.add(50);
	    System.out.println(s);
	    System.out.println(s.peek());
	    System.out.println(s);
	}

}

